    public static String getColorClosestTo(Color c){
        int best = 9999;
        Colors bestc = Colors.BLACK;
        for (Colors cc : Colors.values()){
            int diff = 0;
            diff += Math.abs(cc.color.getRed() - c.getRed()) + Math.abs(cc.color.getGreen() - c.getGreen()) + Math.abs(cc.color.getBlue() - c.getBlue());
            if (diff < best){
                best = diff;
                bestc = cc;
            }
        }
        if (best == 0)
        return bestc.name().toUpperCase();
        else
        return "kinda like " + bestc.name().toUpperCase();
    }

